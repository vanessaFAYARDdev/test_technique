function alertDelete() {
    confirm("Êtes vous sûr de vouloir supprimer ce contrat ?");
}

/////////////////////////////////////////////////////////////////////////////////////////
// CODE PRINCIPAL                                                                      //
/////////////////////////////////////////////////////////////////////////////////////////

$(() => {
    $('#delete').on('click', this.alertDelete.bind(this));
    $('.alert').delay(3000).fadeOut('slow');
});
